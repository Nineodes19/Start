"""
编写程序实现输入不小于0的数x作为一名员工一个月工资数量，
求应交税款y并输出，设应交税款的计算公式如下：
y =
0     x < 5000
(x-5000)*0.03   5000<3<=36000
(x-5000)*0.1-2520  36000<x<=144000
(x-5000)*0.2-16920 x>144000
"""
x = int(input('请输入工资：'))
if x < 5000:
    y = 0
elif x <= 36000:
    y = (x-5000)*0.03
elif x <= 144000:
    y = (x-5000)*0.1-2520
else:
    y = (x-5000)*0.2-16920

print('应交税款为：',y)